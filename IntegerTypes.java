class IntegerTypes
{
	public static void main(String[] args)
	{
		byte byteType = 1;
		System.out.println(byteType);

		short shortType = 12;
		System.out.println(shortType);

		int intType = 123456789;
		System.out.println(intType);

		long longType = 1234567890l;
		System.out.println(longType);
	}
}